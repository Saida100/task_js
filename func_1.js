function defineDay(a){
	
	switch(a){
		case 1:
		console.log("Monday");
		break;
		case 2:
		console.log("Tuesday");
		break;
		case 3:
		console.log("Wednesday");
		break;
		case 4:
		console.log("Thursday");
		break;
		case 5:
		console.log("Friday");
		break;
		case 6:
		console.log("Satirday");
		break;
		case 7:
		console.log("Sunday");
		break;
		default:
				console.log("Invalid number");
				break;

	}

}
console.log("a=50");
defineDay(50);
console.log("a=5");
defineDay(5);
console.log("a=3");
defineDay(3);